		function printTime() {
			date = new Date();
			var hours = date.getHours();
			var minutes = date.getMinutes();
			var seconds = date.getSeconds();

			if (seconds < 10) {
				seconds = "0" + seconds;
			}
			if (minutes < 10) {
				minutes = "0" + minutes;
			}
			if (hours > 12) {
				hours = hours - 12;
				elementTime.innerHTML = hours + " : " + minutes + " : " + seconds + "  PM ";
